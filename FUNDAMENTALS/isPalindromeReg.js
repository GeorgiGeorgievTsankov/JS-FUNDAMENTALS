function isPalindromeReg(str) {
    // Преобразуване на стринга в малки букви
    const cleanStr = str.toLowerCase();
  
    // Изчистване на всички символи, които не са букви
    const alphanumericStr = cleanStr.replace(/[^a-z]/g, '');
  
    // Обръщане на стринга
    const reversedStr = alphanumericStr.split('').reverse().join('');
  
    // Сравняване на оригиналния стринг с обръщания
    return alphanumericStr === reversedStr;
  }
  
  // Примери за използване:
  console.log(isPalindromeReg("level"));       // true
  console.log(isPalindromeReg("A man a plan /a / canal, Panama")); // true
  console.log(isPalindromeReg("hello"));       // false
  
//   Този код използва регулярен израз за търсене и замяна на всички символи в низ, които не са букви от a до z (включително). Да разгледаме по-подробно:

// .replace(): Този метод на низове в JavaScript се използва за замяна на съвпадения на регулярен израз или подниз с друга стойност.

// /[^a-z]/g: Това е регулярен израз, който търси всичко, което не е буква от a до z. Дава следното:

// [^a-z]: Съвпадение с всеки символ, който не е буква от a до z.
// g: Флаг за глобално търсене, който означава, че търсенето трябва да се извърши за всички съвпадения в низа, а не само за първото.
// '': Този втори аргумент представлява замяната, която се извършва за всяко съвпадение. В този случай, заменяме всеки символ, който не е буква от a до z, с празен низ '', т.е. го премахваме.

// Следователно, изразът .replace(/[^a-z]/g, '') по същество оставя само буквите от a до z в низа и премахва всички други символи. Този метод често се използва за "очистване" на низове, като премахва нежелани символи или формати.
